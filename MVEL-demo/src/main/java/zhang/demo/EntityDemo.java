
package zhang.demo;


public class EntityDemo {


    public EntityDemo(String p1, int p2, double p3) {
        this.p1 = p1;
        this.p2 = p2;
        this.p3 = p3;
    }

    public void setP1(String p1) {
        this.p1 = p1;
    }

    public String getP1() {
        return p1;
    }

    private String p1;



    public void setP2(int p2) {
        this.p2 = p2;
    }

    public int getP2() {
        return p2;
    }

    private int p2;


    public double getP3() {
        return p3;
    }

    public void setP3(double p3) {
        this.p3 = p3;
    }

    private double p3;




}
